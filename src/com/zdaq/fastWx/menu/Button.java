package com.zdaq.fastWx.menu;


/**
 * 按钮的基类
 * 
 * @date 2015-01-13
 */
public class Button {
	private String name;
	private String url;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}
	
}
